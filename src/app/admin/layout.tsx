"use client";

import Link from "next/link";
import { useRouter, usePathname } from "next/navigation";
import { useState } from "react";

const NAV_LINKS = [
  { href: "/admin", label: "Dashboard", icon: DashboardIcon },
  { href: "/admin/pages", label: "Pages", icon: PagesIcon },
  { href: "/admin/media", label: "Media", icon: MediaIcon },
  { href: "/admin/studies", label: "Studies", icon: StudiesIcon },
  { href: "/admin/team", label: "Team", icon: TeamIcon },
  // Partners (Option A): the /admin/partners section is retired from the UI —
  // it only fed the deleted carousels. The PartnerLogo data/API are kept (still
  // read by the media-usage scan); the standalone Academic/Industry grids are
  // edited as page-builder image blocks. See GitHub issue #142.
  { href: "/admin/publications", label: "Publications", icon: PublicationsIcon },
  { href: "/admin/contacts", label: "Contact Submissions", icon: ContactsIcon },
  {
    href: "/admin/contact-recipients",
    label: "Contact Recipients",
    icon: RecipientsIcon,
  },
  {
    href: "/admin/site-settings",
    label: "Logos",
    icon: SiteSettingsIcon,
  },
];

export default function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const router = useRouter();
  const pathname = usePathname();
  const [isLoggingOut, setIsLoggingOut] = useState(false);

  // The login page renders its own layout, and the page-preview route renders a
  // bare public page (shown in the editor's preview iframe) — don't wrap either
  // in the admin shell.
  if (pathname === "/admin/login" || pathname.startsWith("/admin/preview")) {
    return <>{children}</>;
  }

  const handleLogout = async () => {
    setIsLoggingOut(true);
    try {
      await fetch("/api/admin/auth/logout", { method: "POST" });
      router.push("/admin/login");
    } catch (error) {
      console.error("Logout failed:", error);
      setIsLoggingOut(false);
    }
  };

  return (
    <div className="min-h-screen bg-[#f5f5f7] text-[#0c0c48]">
      <div className="flex min-h-screen">
        {/* Sidebar */}
        <aside className="w-64 bg-[#0c0c48] text-white flex flex-col">
          <div className="px-6 py-7 border-b border-white/10">
            <p className="text-xs uppercase tracking-[2px] text-white/60">
              HNU
            </p>
            <h1 className="text-xl font-bold mt-1">Admin Console</h1>
          </div>
          <nav className="flex-1 px-3 py-4 space-y-1">
            {NAV_LINKS.map((link) => {
              const isActive =
                link.href === "/admin"
                  ? pathname === "/admin"
                  : pathname.startsWith(link.href);
              const Icon = link.icon;
              return (
                <Link
                  key={link.href}
                  href={link.href}
                  className={`flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                    isActive
                      ? "bg-white/15 text-white"
                      : "text-white/75 hover:bg-white/10 hover:text-white"
                  }`}
                >
                  <Icon />
                  <span>{link.label}</span>
                </Link>
              );
            })}
          </nav>

          <div className="p-4 border-t border-white/10">
            <button
              onClick={handleLogout}
              disabled={isLoggingOut}
              className="w-full inline-flex items-center justify-center gap-2 bg-white/10 text-white px-4 py-2.5 rounded-lg text-sm font-semibold hover:bg-white/20 transition-colors disabled:opacity-60 disabled:cursor-not-allowed"
            >
              <LogoutIcon />
              {isLoggingOut ? "Signing out..." : "Sign out"}
            </button>
          </div>
        </aside>

        {/* Main content */}
        <main className="flex-1 px-10 py-10 overflow-x-auto">
          <div className="max-w-[1400px] mx-auto">{children}</div>
        </main>
      </div>
    </div>
  );
}

